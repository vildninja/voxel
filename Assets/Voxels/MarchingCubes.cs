        public void ProcessChunk(ByteData data, Mesh mesh)
        {
            if (lookup == null || lookup.GetLength(0) < data.Length*2 + 3)
            {
                lookup = new IntInt[data.Length*2 + 3, data.Length*2 + 3, data.Length*2 + 3];
            }
            lookupVersion++;

            vertices.Clear();
            normals.Clear();
            triangles.Clear();
            colors.Clear();
            counters.Clear();

            for (int x = -1; x < data.Length; x++)
            {
                for (int y = -1; y < data.Length; y++)
                {
                    for (int z = -1; z < data.Length; z++)
                    {
                        int configuration = 0;
                        byte target = 1;
                        //for (int i = 0; i < 8; i++)
                        //{
                        //    byte val = data[x + vertexOffset[i, 0], y + vertexOffset[i, 1], z + vertexOffset[i, 2]];
                        //    if (val > target)
                        //    {
                        //        target = val;
                        //    }
                        //}

                        int blend = 1;
                        var color = new Color32();

                        for (int i = 0; i < 8; i++)
                        {
                            byte val = data[x + vertexOffset[i, 0], y + vertexOffset[i, 1], z + vertexOffset[i, 2]];
                            if (val <= target)
                            {
                                configuration |= 1 << i;
                            }

                            if (val > target)
                            {
                                color = Color32.Lerp(color, colorMap[val], 1f/blend);
                                blend++;
                            }
                        }

                        if (configuration > 0 && configuration < 255)
                        {
                            ProcessCube((byte) configuration, new Vector3(x, y, z), color, x < 0 || y < 0 || z < 0);
                        }
                    }
                }
            }

            mesh.Clear();
            mesh.SetVertices(vertices);
            if (SMOOTH)
            {
                for (int i = 0; i < vertices.Count; i++)
                {
                    normals.Add(compositeNormals[i].Normal);
                }
            }
            mesh.SetNormals(normals);
            mesh.SetColors(colors);
            mesh.SetTriangles(triangles, 0);
        }

        private void ProcessCube(byte cube, Vector3 min, Color32 color, bool seemsOnly)
        {
            for (int i = 0; i < 15; i += 3)
            {
                if (triangleTable[cube, i] < 0)
                {
                    break;
                }

                current[0] = verticeTable[triangleTable[cube, i]] + min;
                current[1] = verticeTable[triangleTable[cube, i + 1]] + min;
                current[2] = verticeTable[triangleTable[cube, i + 2]] + min;

                var normal = Vector3.Cross(current[1] - current[0], current[2] - current[0]);
                normal.Normalize();

                for (int j = 0; j < 3; j++)
                {
                    int index = vertices.Count;
                    if (SMOOTH)
                    {
                        var ii = lookup[Mathf.RoundToInt(current[j].x*2) + 2,
                            Mathf.RoundToInt(current[j].y*2) + 2,
                            Mathf.RoundToInt(current[j].z*2) + 2];

                        if (ii.version == lookupVersion)
                        {
                            index = ii.index;
                            compositeNormals[index].Add(normal);
                        }
                        else
                        {
                            ii.index = index;
                            ii.version = lookupVersion;
                            lookup[Mathf.RoundToInt(current[j].x*2) + 2,
                                Mathf.RoundToInt(current[j].y*2) + 2,
                                Mathf.RoundToInt(current[j].z*2) + 2] = ii;
                        }
                        //// there is a higher chance that vertices close to each other
                        //// are added shortly after each other. So I iterate backwards.
                        //for (int k = vertices.Count - 1; k >= 0; k--)
                        //{
                        //    if (Near(vertices[k], current[j]))
                        //    {
                        //        index = k;

                        //        // adjust current values
                        //        //colors[index] = Color32.Lerp(colors[index], )

                        //        compositeNormals[index].Add(normal);

                        //        break;
                        //    }
                        //}
                    }

                    if (index == vertices.Count)
                    {
                        vertices.Add(current[j]);
                        colors.Add(color);
                        if (SMOOTH)
                        {
                            counters.Add(1);
                            if (compositeNormals.Count > index)
                            {
                                compositeNormals[index].Set(normal);
                            }
                            else
                            {
                                compositeNormals.Add(new NormalCalculater(normal));
                            }
                        }
                        else
                        {
                            normals.Add(normal);
                        }
                    }

                    if (!seemsOnly)
                    {
                        triangles.Add(index);
                    }
                }
            }
        }

        private int SoftCeil(float x)
        {
            int round = Mathf.RoundToInt(x);
            if (Mathf.Abs(round - x) < 0.1f)
            {
                return round;
            }
            return Mathf.CeilToInt(x);
        }

